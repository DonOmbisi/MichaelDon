export const Home = () => {
  const [visibleSections, setVisibleSections] = useState([]);
  const [scrollIndicatorHidden, setScrollIndicatorHidden] = useState(false);
  const intro = useRef();
  const projectOne = useRef();
  const projectTwo = useRef();
  const projectThree = useRef();
  const details = useRef();

  useEffect(() => {
    const sections = [intro, projectOne, projectTwo, projectThree, details];

    const sectionObserver = new IntersectionObserver(
      (entries, observer) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            const section = entry.target;
            observer.unobserve(section);
            if (visibleSections.includes(section)) return;
            setVisibleSections(prevSections => [...prevSections, section]);
          }
        });
      },
      { rootMargin: '0px 0px -10% 0px', threshold: 0.1 }
    );

    const indicatorObserver = new IntersectionObserver(
      ([entry]) => {
        setScrollIndicatorHidden(!entry.isIntersecting);
      },
      { rootMargin: '-100% 0px 0px 0px' }
    );

    sections.forEach(section => {
      sectionObserver.observe(section.current);
    });

    indicatorObserver.observe(intro.current);

    return () => {
      sectionObserver.disconnect();
      indicatorObserver.disconnect();
    };
  }, [visibleSections]);

  return (
    <div className={styles.home}>
      <Intro
        id="intro"
        sectionRef={intro}
        scrollIndicatorHidden={scrollIndicatorHidden}
      />
      <ProjectSummary
        id="project-1"
        sectionRef={projectOne}
        visible={visibleSections.includes(projectOne.current)}
        index={1}
        title="Aura3.0 - Autonomous AI Therapist"
        description="An AI-powered mental health platform using advanced NLP and blockchain technology for personalized therapy sessions with privacy protection"
        buttonText="View Live Site"
        buttonLink="https://therapist-agent.vercel.app/"
        model={{
          type: 'laptop',
          alt: 'Aura3.0 AI Therapist Platform',
          textures: [
            {
              srcSet: `/aura.jpeg 1280w, /aura.jpeg 2560w`,
              placeholder: `/aura.jpeg`,
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-2"
        alternate
        sectionRef={projectTwo}
        visible={visibleSections.includes(projectTwo.current)}
        index={2}
        title="Host3 - Web3 Hosting Platform"
        description="Decentralized file storage and hosting platform built with React, Solidity, and IPFS achieving 99.8% uptime"
        buttonText="View Live Site"
        buttonLink="https://host3-blond.vercel.app/"
        model={{
          type: 'phone',
          alt: 'Host3 Web3 Platform',
          textures: [
            {
              srcSet: `/web3.jpeg 375w, /web3.jpeg 750w`,
              placeholder: `/web3.jpeg`,
            },
            {
              srcSet: `/web3.jpeg 375w, /web3.jpeg 750w`,
              placeholder: `/web3.jpeg`,
            },
          ],
        }}
      />
      <ProjectSummary
        id="project-3"
        sectionRef={projectThree}
        visible={visibleSections.includes(projectThree.current)}
        index={3}
        title="Flood-Analyzer - Risk Assessment System"
        description="Comprehensive flood risk assessment system using machine learning and data analytics for environmental monitoring"
        buttonText="View Live Site"
        buttonLink="https://flood-analyzer.vercel.app/"
        model={{
          type: 'laptop',
          alt: 'Flood-Analyzer Risk Assessment Dashboard',
          textures: [
            {
              srcSet: `/flood.jpeg 800w, /flood.jpeg 1920w`,
              placeholder: `/flood.jpeg`,
            },
          ],
        }}
      />
      <Profile
        sectionRef={details}
        visible={visibleSections.includes(details.current)}
        id="details"
      />
      <Footer />
    </div>
  );
};
